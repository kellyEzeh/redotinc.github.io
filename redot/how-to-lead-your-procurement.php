						<p>It's never a better time than now to take an in-depth look at how to be a great procurement professional. While it may be the first time in charge for some, for others it's time to learn how to become a better leader.</p>
						
						<h3>What do procurement managers do?</h3>
						<p>Primarily, you will be responsible for leading the procurement team in getting the goods or services the business needs at the right time and achieving the best value. The exact nature of this varies from organisation to organisation but can include everything from purchasing the energy a factory needs and buying the raw materials to make a product to even sourcing of a recruitment company to supply the staff you need.</p>
						<p>Establish alliances with key suppliers. Best-in-class companies work closely with suppliers long after a deal has been signed. In most circles today, this is called "supplier relationship management." But that implies one-way communication (telling the supplier how to do it). Two-way communication, which requires both buyer and seller to jointly manage the relationship, is more effective. A more appropriate term for this best practice might be "alliance management," with representatives from both parties working together to enhance the buyer/supplier relationship.</p>
						
						<h3>What would be my main priorities?</h3>
						<p>You will need to be a key driver of efficiency for the business, implementing processes that minimise the business waste while ensuring the day-to-day operations of the business runs smoothly. You will need to source suppliers – often on an international basis, negotiate and manage contracts and build key relationships with both suppliers and the internal teams. Procurement managers need to stay on top of new trends, regulation and technology as they often will be expected to lead on innovation and cost savings as these ultimately can create a competitive advantage for the business.</p>
						<p>The four primary objectives of an effective alliance management program with key suppliers include:</p>
						<ul>
						<li><i class="fa fa-check"></i> Provide a mechanism to ensure that the relationship stays healthy and vibrant</li>
						<li><i class="fa fa-check"></i> Create a platform for problem resolution</li>
						<li><i class="fa fa-check"></i> Develop continuous improvement goals with the objective of achieving value for both parties</li>
						<li><i class="fa fa-check"></i> Ensure that performance measurement objectives are achieved</li>
					</ul>
					<p>With a sound alliance management program in place, you will be equipped to use the talents of your supply base to create sustained value while constantly seeking improvement.
